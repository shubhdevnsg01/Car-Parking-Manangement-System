const express = require('express');
const validate = require('../../middlewares/validate');
const parkingValidation = require('../../validations/parking.validation');
const parkingController = require('../../controllers/parking.controller');
const auth = require('../../middlewares/auth');

const router = express.Router();

router.route('/').post(parkingController.createParking).get(parkingController.getParkings);

router.route('/park-car').post(parkingController.parkCar);
router.route('/unpark-car').post(auth(), parkingController.unparkCar);

router
  .route('/:parkingId')
  .get(parkingController.getParking)
  .patch(parkingController.updateParking)
  .delete(parkingController.deleteParking);

module.exports = router;

